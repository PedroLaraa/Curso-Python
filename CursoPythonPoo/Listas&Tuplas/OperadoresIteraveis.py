
idades = [32, 35, 76, 43, 57, 12, 21]

print('Método sorted(arr): ',sorted(idades))

print('Método sorted(arr reverse=True):', sorted(idades, reverse=True))

# Método arr.sort() aplica-se no estado da arr, altera o arr original
# Método sorted(arr) aplica-se em um local apenas, não altera a arr original

